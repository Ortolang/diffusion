package fr.ortolang.diffusion.client;

@SuppressWarnings("serial")
public class OrtolangClientException  extends Exception {

	public OrtolangClientException() {
		super();
	}

	public OrtolangClientException(String message, Throwable cause) {
		super(message, cause);
	}

	public OrtolangClientException(String message) {
		super(message);
	}

	public OrtolangClientException(Throwable cause) {
		super(cause);
	}

}
