package fr.ortolang.diffusion.rest.datatables;

@SuppressWarnings("serial")
public class DataTableRequestParamsException  extends Exception {

	public DataTableRequestParamsException() {
		super();
	}

	public DataTableRequestParamsException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataTableRequestParamsException(String message) {
		super(message);
	}

	public DataTableRequestParamsException(Throwable cause) {
		super(cause);
	}

}