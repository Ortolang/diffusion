package fr.ortolang.diffusion;

@SuppressWarnings("serial")
public class OrtolangException  extends Exception {

	public OrtolangException() {
		super();
	}

	public OrtolangException(String message, Throwable cause) {
		super(message, cause);
	}

	public OrtolangException(String message) {
		super(message);
	}

	public OrtolangException(Throwable cause) {
		super(cause);
	}

}
