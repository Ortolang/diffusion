package fr.ortolang.diffusion;

@SuppressWarnings("serial")
public class DiffusionServiceException  extends Exception {

	public DiffusionServiceException() {
		super();
	}

	public DiffusionServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public DiffusionServiceException(String message) {
		super(message);
	}

	public DiffusionServiceException(Throwable cause) {
		super(cause);
	}

}
