package fr.ortolang.diffusion.store.index;

@SuppressWarnings("serial")
public class IndexStoreServiceException extends Exception {

	public IndexStoreServiceException() {
		super();
	}

	public IndexStoreServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public IndexStoreServiceException(String message) {
		super(message);
	}

	public IndexStoreServiceException(Throwable cause) {
		super(cause);
	}

}